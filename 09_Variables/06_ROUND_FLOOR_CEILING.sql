-- [SQL Server] Variáveis
-- ROUND, FLOOR e CEILING - Funções de Arredondamento

-- Operações com números

SELECT 10+20
SELECT 20-5
SELECT 31*40
SELECT 431.0/23

-- ROUND

SELECT ROUND(18.739130, 2)

-- ROUND (Truncar)

SELECT ROUND(18.739130, 2, 1)

-- FLOOR

SELECT FLOOR(18.739130)

-- CEILING

SELECT CEILING(18.739130)