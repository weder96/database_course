CURRENT_TIMESTAMP, --2025-04-13 18:01:39.560
SYSDATETIME(), --2025-04-13 18:01:39.5612118
SYSDATETIMEOFFSET(),--2025-04-13 18:01:39.5612118 +00:00
SYSUTCDATETIME()  --2025-04-13 18:01:39.5612118



